package java0622;

public class ex03 {
	public static void main(String[] args) {
		int num1 = 7;
		int num2 = 3;
		
		System.out.println("더하기 결과 : "+(num1+num2));
		System.out.println("빼기 결과 : "+(num1-num2));
		System.out.println("곱하기 결과 : "+(num1*num2));
		System.out.println("나누기 결과 : "+(double)num1/num2);
	}
}
